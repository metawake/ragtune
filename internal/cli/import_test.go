func TestParseJSONQueries_Basic(t *testing.T) {
	tmpDir := t.TempDir()
	jsonPath := filepath.Join(tmpDir, "queries.json")

	jsonContent := `{
  "queries": [
    {"id": "q1", "text": "How do I reset my password?", "relevant_docs": ["password.md"]},
    {"id": "q2", "text": "What are rate limits?", "relevant_docs": ["limits.md", "quotas.md"]}
  ]
}`

	os.WriteFile(jsonPath, []byte(jsonContent), 0644)

	queries, err := parseJSONQueries(jsonPath)
	if err != nil {
		t.Fatalf("parseJSONQueries failed: %v", err)
	}

	if len(queries) != 2 {
		t.Fatalf("expected 2 queries, got %d", len(queries))
	}

	if queries[0].ID != "q1" {
		t.Errorf("query 0 ID: got %q", queries[0].ID)
	}
	if queries[0].Text != "How do I reset my password?" {
		t.Errorf("query 0 text: got %q", queries[0].Text)
	}
	if len(queries[1].RelevantDocs) != 2 {
		t.Errorf("query 1 relevant docs count: got %d", len(queries[1].RelevantDocs))
	}
}

