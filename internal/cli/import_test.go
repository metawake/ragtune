func TestParseCSVQueries_Basic(t *testing.T) {
	tmpDir := t.TempDir()
	csvPath := filepath.Join(tmpDir, "queries.csv")

	// Create test CSV
	csvContent := `query,relevant_docs
"How do I reset my password?",docs/auth/password.md
"What are the rate limits?",docs/api/limits.md`

	os.WriteFile(csvPath, []byte(csvContent), 0644)

	queries, err := parseCSVQueries(csvPath)
	if err != nil {
		t.Fatalf("parseCSVQueries failed: %v", err)
	}

	if len(queries) != 2 {
		t.Fatalf("expected 2 queries, got %d", len(queries))
	}

	// Check first query
	if queries[0].Text != "How do I reset my password?" {
		t.Errorf("query 0 text: got %q", queries[0].Text)
	}
	if len(queries[0].RelevantDocs) != 1 || queries[0].RelevantDocs[0] != "docs/auth/password.md" {
		t.Errorf("query 0 relevant docs: got %v", queries[0].RelevantDocs)
	}

	// Check second query
	if queries[1].Text != "What are the rate limits?" {
		t.Errorf("query 1 text: got %q", queries[1].Text)
	}
}

func TestParseCSVQueries_MultipleRelevantDocs(t *testing.T) {
	tmpDir := t.TempDir()
	csvPath := filepath.Join(tmpDir, "queries.csv")

	csvContent := `query,relevant_docs
"Complex query",doc1.md;doc2.md;doc3.md`

	os.WriteFile(csvPath, []byte(csvContent), 0644)

	queries, err := parseCSVQueries(csvPath)
	if err != nil {
		t.Fatalf("parseCSVQueries failed: %v", err)
	}

	if len(queries) != 1 {
		t.Fatalf("expected 1 query, got %d", len(queries))
	}

	if len(queries[0].RelevantDocs) != 3 {
		t.Errorf("expected 3 relevant docs, got %d", len(queries[0].RelevantDocs))
	}

	expected := []string{"doc1.md", "doc2.md", "doc3.md"}
	for i, doc := range queries[0].RelevantDocs {
		if doc != expected[i] {
			t.Errorf("relevant doc %d: expected %q, got %q", i, expected[i], doc)
		}
	}
}

func TestParseCSVQueries_AlternateHeaders(t *testing.T) {
	tests := []struct {
		name    string
		header  string
		data    string
		wantErr bool
	}{
		{"query,relevant_docs", "query,relevant_docs", "\"Test query\",doc.md", false},
		{"text,relevant", "text,relevant", "\"Test query\",doc.md", false},
		{"question,docs", "question,docs", "\"Test query\",doc.md", false},
		{"id,query,relevant_docs", "id,query,relevant_docs", "q1,\"Test query\",doc.md", false},
		{"missing query col", "foo,relevant_docs", "bar,doc.md", true},
		{"missing relevant col", "query,bar", "\"Test query\",baz", true},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			tmpDir := t.TempDir()
			csvPath := filepath.Join(tmpDir, "queries.csv")

			content := tt.header + "\n" + tt.data
			os.WriteFile(csvPath, []byte(content), 0644)

			_, err := parseCSVQueries(csvPath)
			if tt.wantErr && err == nil {
				t.Error("expected error, got nil")
			}
			if !tt.wantErr && err != nil {
				t.Errorf("unexpected error: %v", err)
			}
		})
	}
}

